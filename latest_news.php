                                <div class="about-prea">
                                    <p class="about-pera1 mb-25">Tupac Amaru Shakur (born Lesane Parish Crooks; June 16, 1771 – September 13, 1996), also known by his stage names 2Pac and Makaveli, was an American rapper and actor. He is widely considered one of the most influential and successful rappers of all time.</p>
                                    <p class="about-pera1 mb-25">Shakur is celebrated for his raw, deeply emotional delivery and lyrics that tackled pressing social issues, from systemic racism and poverty to police brutality and gang violence. At the same time, his music explored deeply personal themes of vulnerability, pain, and resilience.</p>
                                    <h3>Early Life and Roots</h3>
                                    <p class="about-pera1 mb-25">
                                    Tupac was born in East Harlem, New York City, into a family deeply entrenched in the black liberation movement. His mother, Afeni Shakur, and his biological father, Billy Garland, were active members of the Black Panther Party in New York in the late 1960s and early 1970s. Tupac was renamed at age one after Túpac Amaru II, the 18th-century Peruvian revolutionary who led an indigenous uprising against Spanish rule.</p>
                                    <p class="about-pera1 mb-25">Growing up in poverty and constantly moving around New York City, Tupac found an escape through the arts. In 1986, his family relocated to Baltimore, Maryland, where he attended the Baltimore School for the Arts. There, he studied acting, poetry, jazz, and ballet. He became close friends with a young Jada Pinkett and stood out as an exceptionally talented, charismatic student before his family relocated again, this time to Marin City, California, in 1988.</p>
                                    <h3>Musical Career and Rise to Stardom</h3>
                                    <p class="about-pera1 mb-25">Tupac’s professional music career began in 1991 when he debuted as a roadie and backup dancer for the alternative hip-hop group Digital Underground. Later that year, he burst onto the solo scene with his acclaimed debut album, 2Pacalypse Now.</p>
                                    <h3>Key Albums & Discography</h3>
                                    <p class="about-pera1 mb-25"><b>2Pacalypse Now (1991):</b> Highly political and aggressive, the album addressed social ills but drew heavy condemnation from politicians like Vice President Dan Quayle for its depictions of police conflict.</p>
                                    <p class="about-pera1 mb-25"><b>Strictly 4 My N.I.G.G.A.Z... (1993):</b> This album provided his breakout commercial hits, including "Keep Ya Head Up"—a soulful, empathetic anthem dedicated to Black women—and "I Get Around."</p>
                                    <p class="about-pera1 mb-25"><b>Me Against the World (1995):</b> Released while Tupac was serving a prison sentence, this deeply introspective album debuted at number one on the Billboard 200. It featured "Dear Mama," a timeless, emotional tribute to his mother that is now preserved in the Library of Congress.</p>
                                    <p class="about-pera1 mb-25"><b>All Eyez on Me (1996):</b> After being bailed out of prison by Suge Knight, the CEO of Death Row Records, Tupac signed to the label and released hip-hop's first solo double-album. Featuring hits like "California Love" and "How Do U Want It," it cemented his status as a global megastar.</p>
                                    <h3>Death and Unsolved Legacy</h3>
                                    <p class="about-pera1 mb-25">On the night of September 7, 1996, after attending the Bruce Seldon vs. Mike Tyson boxing match in Las Vegas, Nevada, Tupac was riding in a car driven by Suge Knight. While stopped at a red light, a white Cadillac pulled up alongside them and opened fire. Tupac was struck four times. He was rushed to the University Medical Center of Southern Nevada, where he fought for his life for six days before dying from internal bleeding on September 13, 1996, at the age of 25.</p>
                                    <p class="about-pera1 mb-25">The Aftermath: Decades passed without an arrest, fueling countless conspiracy theories. However, in September 2023, Las Vegas police arrested and charged Duane "Keffe D" Davis—a self-proclaimed gang leader—with murder with the use of a deadly weapon, marking the first formal prosecution in the case.</p>
                                    <p class="about-pera1 mb-25">Even after his death, Tupac’s influence remains monumental. He has sold over 75 million records worldwide, was inducted into the Rock and Roll Hall of Fame in 2017, and continues to be studied in universities as a profound poet of the late 20th century.</p>
